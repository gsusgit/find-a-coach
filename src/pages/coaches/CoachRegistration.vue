<template>
  <section>
    <base-card>
      <h2>Register as a coach now!</h2>
      <coach-form @add-coach="addCoach"></coach-form>
    </base-card>
  </section>
</template>

<script>
import CoachForm from '../../components/coaches/CoachForm.vue'

export default {
  components: {
    CoachForm
  },
  methods: {
    addCoach(data) {
      this.$store.dispatch('coaches/addCoach', data)
      this.$router.replace('/coaches')
    }
  }
};
</script>
